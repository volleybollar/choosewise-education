#!/usr/bin/env node
/*
  Parses the extracted NLM HTML and produces styles-sv.json.

  Input:  /tmp/nlm-extracted.html  (from textutil step)
          The RTF was converted to HTML by macOS textutil, where the actual
          NotebookLM HTML is entity-encoded inside <p> tags. This script
          first decodes that wrapper, then extracts the JS data arrays
          (cloudImages and actualPrompts) directly from the script block,
          because the cards are dynamically generated by JS — no DOM elements.

  Output: ../sv/notebooklm-stilar/styles-sv.json

  Usage:  node scripts/parse-nlm-html.js /tmp/nlm-extracted.html
*/

import fs from 'node:fs';
import path from 'node:path';
import { load } from 'cheerio';

const [,, inputPath] = process.argv;
if (!inputPath) {
  console.error('Usage: node parse-nlm-html.js <input.html>');
  process.exit(1);
}

// ── Step 1: Read the outer RTF-converted HTML ─────────────────────────────────
const outerHtml = fs.readFileSync(inputPath, 'utf-8');
const $outer = load(outerHtml);

// The actual NLM HTML is entity-encoded text inside the <body> of the wrapper.
// Extract all text content from body and decode HTML entities.
let innerHtml = $outer('body').text();

// textutil sometimes wraps lines — the text extraction gives us the decoded HTML.
// Write to a temp location so we can double-check if needed.
const innerPath = '/tmp/nlm-inner-parsed.html';
fs.writeFileSync(innerPath, innerHtml, 'utf-8');
console.log(`Inner HTML decoded: ${innerHtml.length} chars → ${innerPath}`);

// ── Step 2: Extract cloudImages from JS ──────────────────────────────────────
function extractJsObject(source, varName) {
  // Match: varName = { ... };  (handles both const/let/var and plain assignment)
  const startRe = new RegExp(`(?:const|let|var)?\\s*${varName}\\s*=\\s*\\{`);
  const startMatch = source.match(startRe);
  if (!startMatch) return null;
  const startIdx = source.indexOf(startMatch[0]) + startMatch[0].length - 1; // at the {
  // Walk forward to find matching }
  let depth = 0;
  let i = startIdx;
  while (i < source.length) {
    if (source[i] === '{') depth++;
    else if (source[i] === '}') {
      depth--;
      if (depth === 0) break;
    }
    i++;
  }
  const objText = source.slice(startIdx, i + 1);
  return objText;
}

// Parse a JS object literal with numeric keys and string values.
// Handles multi-line values and nested quotes.
function parseJsStringObj(objText) {
  const result = {};
  // Remove outer braces
  const inner = objText.slice(1, -1);
  // We use a state machine to split by top-level commas after numeric-key: "value" pairs
  let i = 0;
  while (i < inner.length) {
    // Skip whitespace/newlines
    while (i < inner.length && /\s/.test(inner[i])) i++;
    if (i >= inner.length) break;

    // Read key (integer)
    const keyStart = i;
    while (i < inner.length && /\d/.test(inner[i])) i++;
    if (i === keyStart) { i++; continue; } // skip non-numeric
    const key = parseInt(inner.slice(keyStart, i), 10);

    // Skip whitespace + colon
    while (i < inner.length && (inner[i] === ' ' || inner[i] === ':')) i++;

    // Read string value — may be delimited by " or '
    // Also handle template literals (`) if any
    const delim = inner[i];
    if (delim !== '"' && delim !== "'" && delim !== '`') {
      // Skip to next comma
      while (i < inner.length && inner[i] !== ',') i++;
      i++; continue;
    }
    i++; // past opening delimiter
    let value = '';
    while (i < inner.length) {
      if (inner[i] === '\\') {
        // escape sequence
        i++;
        const esc = inner[i];
        if (esc === 'n') value += '\n';
        else if (esc === 't') value += '\t';
        else if (esc === '"' || esc === "'" || esc === '\\' || esc === '`') value += esc;
        else value += '\\' + esc;
        i++;
      } else if (inner[i] === delim) {
        i++; // past closing delimiter
        break;
      } else {
        // Handle curly-quote entities that may appear in the source
        value += inner[i];
        i++;
      }
    }

    result[key] = value;

    // Skip to next comma
    while (i < inner.length && inner[i] !== ',') i++;
    i++; // past comma
  }
  return result;
}

const cloudImagesText = extractJsObject(innerHtml, 'cloudImages');
if (!cloudImagesText) {
  console.error('ERROR: Could not find cloudImages in the HTML. Check selector.');
  process.exit(1);
}
const cloudImages = parseJsStringObj(cloudImagesText);
console.log(`cloudImages: ${Object.keys(cloudImages).length} entries`);

const actualPromptsText = extractJsObject(innerHtml, 'actualPrompts');
if (!actualPromptsText) {
  console.error('ERROR: Could not find actualPrompts in the HTML. Check selector.');
  process.exit(1);
}
const actualPrompts = parseJsStringObj(actualPromptsText);
console.log(`actualPrompts: ${Object.keys(actualPrompts).length} entries`);

// ── Step 3: Build styles array ────────────────────────────────────────────────
const styles = [];
const total = Math.max(
  ...Object.keys(cloudImages).map(Number),
  ...Object.keys(actualPrompts).map(Number)
);

for (let idx = 1; idx <= total; idx++) {
  const num = String(idx).padStart(3, '0');
  styles.push({
    id: `style-${idx}`,
    title: `Stil ${idx}`,
    category: '',
    image: `/assets/notebooklm-images/style-${num}.webp`,
    image_sources: [
      `/assets/notebooklm-images/style-${num}-400w.webp 400w`,
      `/assets/notebooklm-images/style-${num}-800w.webp 800w`,
      `/assets/notebooklm-images/style-${num}.webp 1200w`,
    ],
    original_src: cloudImages[idx] || '',
    prompt: actualPrompts[idx] || '',
  });
}

console.log(`\nBuilt ${styles.length} style entries.`);
console.log('Sample entry 1:');
console.log('  title:', styles[0].title);
console.log('  original_src:', styles[0].original_src.slice(0, 60));
console.log('  prompt (first 100):', styles[0].prompt.slice(0, 100));
console.log('Sample entry 70:');
console.log('  title:', styles[69].title);
console.log('  prompt (first 100):', styles[69].prompt.slice(0, 100));
console.log('Sample entry 140:');
console.log('  title:', styles[139].title);
console.log('  prompt (first 100):', styles[139].prompt.slice(0, 100));

// ── Step 4: Write output ──────────────────────────────────────────────────────
// Output path is relative to project root (one level up from scripts/)
const outPath = path.join(path.dirname(process.cwd()), 'sv', 'notebooklm-stilar', 'styles-sv.json');
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, JSON.stringify(styles, null, 2), 'utf-8');
console.log(`\nWrote ${styles.length} entries to ${outPath}`);
